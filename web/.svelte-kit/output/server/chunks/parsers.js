const aiApi = {
  /** Non-streaming evaluate — returns verdict + feedback JSON. */
  evaluate(profileId, card, userAnswer) {
    return api.post(`/profiles/${profileId}/ai/evaluate`, { card, userAnswer });
  },
  /** Stream the answer review (SSE). Returns a cancel function. */
  streamReview(profileId, card, userAnswer, onDelta, onComplete, onError) {
    return streamPost(
      `/profiles/${profileId}/ai/review`,
      { card, userAnswer },
      onDelta,
      onComplete,
      onError
    );
  },
  /** Stream a tutor response (SSE). Returns a cancel function. */
  streamTutor(profileId, card, history, message, onDelta, onComplete, onError) {
    return streamPost(
      `/profiles/${profileId}/ai/tutor`,
      { card, history, message },
      onDelta,
      onComplete,
      onError
    );
  },
  /** Send malformed import text to the AI; returns fixed JSON string. */
  fixImport(profileId, text) {
    return api.post(`/profiles/${profileId}/ai/fix-import`, { text });
  }
};
function stripFences(text) {
  return text.replace(/```(?:json)?\s*\n?|\n?```/gi, "").trim();
}
function extractJson(text) {
  const arrStart = text.indexOf("[");
  const arrEnd = text.lastIndexOf("]");
  if (arrStart !== -1 && arrEnd > arrStart) {
    return text.slice(arrStart, arrEnd + 1);
  }
  const objStart = text.indexOf("{");
  const objEnd = text.lastIndexOf("}");
  if (objStart !== -1 && objEnd > objStart) {
    return text.slice(objStart, objEnd + 1);
  }
  return null;
}
function repairLooseJson(text) {
  const kvLine = /^(\s*"(?:[^"\\]|\\.)*"\s*:\s*)"(.*)"(\s*,?\s*)$/;
  const repaired = text.split("\n").map((line) => {
    const m = line.match(kvLine);
    if (!m) return line;
    const [, prefix, rawValue, suffix] = m;
    const fixed = rawValue.replace(/\\"/g, '"').replace(/"/g, '\\"');
    return `${prefix}"${fixed}"${suffix}`;
  }).join("\n");
  return repaired.replace(/,(\s*[}\]])/g, "$1");
}
function parseJson(text) {
  const errors = [];
  const cleaned = stripFences(text);
  let parsed;
  try {
    parsed = JSON.parse(cleaned);
  } catch {
    const extracted = extractJson(cleaned);
    const candidate = extracted ?? cleaned;
    try {
      parsed = JSON.parse(candidate);
    } catch {
      try {
        parsed = JSON.parse(repairLooseJson(candidate));
      } catch {
        return {
          cards: [],
          errors: [
            `JSON non valido anche dopo tentativo di riparazione automatica. Prova a rigenerare con l'AI o correggi manualmente le virgolette interne (usa \\" invece di ").`
          ]
        };
      }
    }
  }
  const obj = typeof parsed === "object" && parsed !== null ? parsed : null;
  if (obj && "decks" in obj) {
    const data = parsed;
    const cards = [];
    for (const deck of data.decks) {
      if (Array.isArray(deck.cards)) {
        cards.push(...deck.cards.filter((c) => c.question && c.answer));
      }
    }
    return { cards, errors };
  }
  if (obj && Array.isArray(obj.cards)) {
    const cards = mapCards(obj.cards, errors);
    return {
      cards,
      errors,
      name: typeof obj.name === "string" ? obj.name.trim() : void 0,
      description: typeof obj.description === "string" ? obj.description.trim() : void 0
    };
  }
  if (Array.isArray(parsed)) {
    return { cards: mapCards(parsed, errors), errors };
  }
  return {
    cards: [],
    errors: ["Formato JSON non riconosciuto. Usa un array, un oggetto mazzo o l'export Falschcard."]
  };
}
function mapCards(arr, errors) {
  const cards = [];
  for (let i = 0; i < arr.length; i++) {
    const item = arr[i] ?? {};
    if (!item.question || !item.answer) {
      errors.push(`Elemento ${i + 1}: "question" o "answer" mancante — carta saltata`);
      continue;
    }
    cards.push({
      question: String(item.question),
      answer: String(item.answer),
      explanation: item.explanation ? String(item.explanation) : void 0
    });
  }
  return cards;
}
